/**
 * Playwright E2E tests for Gemini Flash fallback in Farm Advisory
 */

const { chromium } = require("playwright");

const BASE = process.env.BASE_URL || "https://spices.govihublk.com";
const TIMEOUT = 90_000;

let browser, page;

async function setup() {
  browser = await chromium.launch({ headless: true });
  const ctx = await browser.newContext({ ignoreHTTPSErrors: true });
  page = await ctx.newPage();
  page.setDefaultTimeout(TIMEOUT);

  // Login as farmer via beta-login
  await page.goto(`${BASE}/en/auth/beta-login`);
  await page.waitForLoadState("networkidle");
  await page.waitForTimeout(2000);

  // Fill login form — username field is input[type="text"] with autocomplete="username"
  await page.locator('input[autocomplete="username"]').fill("nuwan");
  await page.locator('input[type="password"]').first().fill("Nuwan-Super9635");

  // Submit
  await page.locator('form button[type="submit"], form button:not([type])').first().click();
  await page.waitForTimeout(5000);
  await page.waitForLoadState("networkidle");

  // Now navigate to advisory page (nuwan is admin, but advisory is farmer route)
  // We need a farmer account. Let's register one if needed or use direct API token injection
  // Actually admin can access advisory too (require_role("farmer", "admin"))
  await page.goto(`${BASE}/en/farmer/advisory`);
  await page.waitForLoadState("networkidle");
  await page.waitForTimeout(3000);

  // Check we're on the advisory page
  const pageTitle = await page.textContent("h1").catch(() => "");
  console.log("Page title:", pageTitle);
}

async function teardown() {
  if (browser) await browser.close();
}

// Helper: send a question and wait for assistant response
async function askQuestion(question) {
  // The input field uses a translated placeholder — match by element type in the chat area
  const input = page.locator('input[class*="border"]').last();
  await input.fill(question);

  // Click Send button
  const sendBtn = page.locator('button').filter({ hasText: /send/i }).first();
  await sendBtn.click({ force: true });

  // Wait for loading animation to appear and then disappear
  await page.waitForTimeout(2000);
  try {
    await page.waitForFunction(() => {
      const bouncing = document.querySelectorAll('.animate-bounce');
      return bouncing.length === 0;
    }, { timeout: TIMEOUT });
  } catch {
    console.log("  WARN: Loading animation timeout");
  }
  await page.waitForTimeout(1500);
}

// Test 1: RAG hit — question about black pepper (should be in knowledge base)
async function testRagHit() {
  console.log("Test 1: RAG hit — black pepper question...");
  await askQuestion("How do I prevent black pepper wilt disease?");

  // Check for either badge type
  const greenBadge = page.locator('span:has-text("From GoviHub Knowledge Base")').last();
  const amberBadge = page.locator('span:has-text("AI-Generated Answer")').last();

  const greenVisible = await greenBadge.isVisible({ timeout: 5000 }).catch(() => false);
  const amberVisible = await amberBadge.isVisible({ timeout: 3000 }).catch(() => false);

  if (greenVisible) {
    console.log("  PASS: Green knowledge base badge visible");
    return true;
  } else if (amberVisible) {
    console.log("  PASS: Got AI-Generated badge (KB may not have pepper-specific data)");
    return true;
  }
  console.log("  FAIL: No source type badge found");
  return false;
}

