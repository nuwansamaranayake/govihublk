// Test 4: Verify mixed source types in conversation
async function testMixedSourceTypes() {
  console.log("Test 4: Verify mixed source types in conversation...");

  const greenCount = await page.locator('span:has-text("From GoviHub Knowledge Base")').count();
  const amberCount = await page.locator('span:has-text("AI-Generated Answer")').count();

  console.log(`  INFO: ${greenCount} knowledge base, ${amberCount} AI-generated answers`);

  const total = greenCount + amberCount;
  if (total >= 2) {
    console.log("  PASS: Multiple source type badges present");
  } else {
    console.log("  WARN: Expected at least 2 badges, got " + total);
  }

  // Verify assistant messages have text content
  const assistantMsgs = page.locator('.rounded-2xl.bg-white.border .text-sm');
  const count = await assistantMsgs.count();
  if (count > 0) {
    console.log("  PASS: " + count + " assistant messages with content");
    return true;
  }
  console.log("  FAIL: No assistant message content found");
  return false;
}

// Test 5: API-level test — verify source_type field in response
async function testApiSourceType() {
  console.log("Test 5: API-level source_type verification...");

  // Get token from sessionStorage
  const token = await page.evaluate(() => sessionStorage.getItem("govihub_token"));
  if (!token) {
    console.log("  FAIL: No auth token in sessionStorage");
    return false;
  }

  // Test off-topic question via API
  const response = await page.evaluate(async (t) => {
    const res = await fetch("/api/v1/advisory/ask", {
      method: "POST",
      headers: { "Authorization": `Bearer ${t}`, "Content-Type": "application/json" },
      body: JSON.stringify({ question: "What is quantum computing?", language: "en" }),
    });
    return res.json();
  }, token);

  if (response.source_type === "ai_generated") {
    console.log("  PASS: Off-topic question returned source_type='ai_generated'");
  } else {
    console.log("  WARN: Expected 'ai_generated', got '" + response.source_type + "'");
  }

  // Test in-domain question via API
  const response2 = await page.evaluate(async (t) => {
    const res = await fetch("/api/v1/advisory/ask", {
      method: "POST",
      headers: { "Authorization": `Bearer ${t}`, "Content-Type": "application/json" },
      body: JSON.stringify({ question: "How to prevent pepper wilt?", language: "en" }),
    });
    return res.json();
  }, token);

  if (response2.source_type === "knowledge_base") {
    console.log("  PASS: Spice question returned source_type='knowledge_base'");
  } else {
    console.log("  WARN: Expected 'knowledge_base', got '" + response2.source_type + "'");
  }

  // Verify source_type field exists
  if ("source_type" in response && "source_type" in response2) {
    console.log("  PASS: source_type field present in both responses");
    return true;
  }
  console.log("  FAIL: source_type field missing from response");
  return false;
}

(async () => {
  let passed = 0;
  let failed = 0;
  const total = 5;

  try {
    await setup();
    console.log("\n=== Gemini Flash Fallback Tests ===\n");

    const tests = [
      testRagHit,
      testRagMissEnglish,
      testRagHitTurmeric,
      testMixedSourceTypes,
      testApiSourceType,
    ];

    for (const test of tests) {
      try {
        const result = await test();
        if (result !== false) passed++;
        else failed++;
      } catch (err) {
        console.log(`  ERROR: ${err.message}`);
        failed++;
      }
    }

    console.log(`\n=== Results: ${passed}/${total} passed, ${failed} failed ===\n`);
  } catch (err) {
    console.error("Setup failed:", err.message);
    failed = total;
  } finally {
    await teardown();
    process.exit(failed > 0 ? 1 : 0);
  }
})();
